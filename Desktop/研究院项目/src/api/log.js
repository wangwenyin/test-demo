import request from '@/utils/request'
// 日志列表
export function getLogs(params) {
  return request({
    url: '/logs',
    method: 'get',
    params
  })
}

// 获取日志详情
export function getLogsById(id, params) {
  return request({
    url: `/logs/${id}`,
    method: 'get',
    params
  })
}
// 查询明细
export function getLogDetail(params) {
  return request({
    url: '/logs/detail',
    method: 'get',
    params
  })
}

// 统计明细
export function getLogStat(params) {
  return request({
    url: '/logs/stat',
    method: 'get',
    params
  })
}
